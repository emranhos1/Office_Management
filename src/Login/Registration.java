    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Connection con = null;
        PreparedStatement pstm = null;

        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/admin", "root", "123");
            pstm = con.prepareStatement("insert into admins(a_f_name, a_l_name, a_username, a_email, a_pass, a_gender, a_phone, a_address) values(?,?,?,?,?,?,?,?)");
            pstm.setString(1, jTextField1.getText());
            pstm.setString(2, jTextField2.getText());
            pstm.setString(3, jTextField3.getText());
            pstm.setString(4, jTextField4.getText());
            pstm.setString(5, jPasswordField1.getText());
            pstm.setString(6, gender);
            pstm.setString(7, jTextField5.getText());
            pstm.setString(8, jTextArea1.getText());

            int count = 0;
            count = pstm.executeUpdate();
            if (count > 0) {
                JOptionPane.showMessageDialog(rootPane, "Registration Successfully Completed\nnow You can login.");
            } else {
                JOptionPane.showMessageDialog(rootPane, "ERROR to Data Inserted");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
        finally{
            try {
                con.close();
                pstm.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
